    void getData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try{
            Statement stat = (Statement)koneksi.con.createStatement();
            String sql = "select * from wali_kelas";
            ResultSet rs = stat.executeQuery(sql);
            
            while(rs.next()){ 
                Object[] obj = new Object[4];
                obj[0] = rs.getString("nip");
                obj[1] = rs.getString("nama");
                if (rs.getString("jk").equals("L")){
                    obj[2] = "Laki Laki";
                }else{
                    obj[2] = "Perempuan";
                }
                obj[3] = rs.getString("alamat");
                model.addRow(obj);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    void getDataWaliKelas(String nip){
        
        try{
            Statement stat = (Statement)koneksi.con.createStatement();
            String sql = "select * from wali_kelas where nip = '"+nip+"'";
            ResultSet rs = stat.executeQuery(sql);
            if (rs.first()){
                nipLama = nip;
                jTextField1.setText(rs.getString("nip"));
                jTextField2.setText(rs.getString("nama"));
                jTextArea1.setText(rs.getString("alamat"));
                if (rs.getString("jk").equals("L")){
                    jComboBox1.setSelectedIndex(0);
                }else{
                    jComboBox1.setSelectedIndex(1);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    void simpan(String nip, String nama, String alamat, char jk){
        try{
            Statement stat = (Statement)koneksi.con.createStatement();
            String sql = "INSERT INTO wali_kelas"
                    + "(nip, nama, jk, alamat) "
                    + "values ('"+nip+"', '"+nama+"', '"+jk+"', '"+alamat+"')";
            int result = stat.executeUpdate(sql);
            if (result == 1){
                JOptionPane.showMessageDialog(this, "Data "+nip+" Berhasil di tambahkan!");
                getData();
                reset();
            }else{
                JOptionPane.showMessageDialog(this, "Data "+nip+" Gagal di tambahkan!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    void rubah(String nip, String nama, String alamat, char jk){
        try{
            Statement stat = (Statement)koneksi.con.createStatement();
            String sql = "UPDATE wali_kelas "
                    + "set nip = '"+nip+"', "
                    + "nama = '"+nama+"', "
                    + "alamat = '"+alamat+"', "
                    + "jk = '"+jk+"' where nip = '"+nipLama+"' ";
            int result = stat.executeUpdate(sql);
            if (result == 1){
                JOptionPane.showMessageDialog(this, "Data "+nip+" Berhasil di Rubah!");
                getData();
                nipLama = null;
                reset();
            }else{
                JOptionPane.showMessageDialog(this, "Data "+nip+" Gagal di Rubah!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    void reset(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextArea1.setText("");
        jComboBox1.setSelectedIndex(0);
    }
    
    void hapus(String nip){
        try{
            Statement stat = (Statement)koneksi.con.createStatement();
            String sql = "delete from wali_kelas where nip = '"+nip+"'";
            int result = stat.executeUpdate(sql);
            if (result == 1){
                JOptionPane.showMessageDialog(this, "Data "+nip+" Berhasil di hapus !");
                reset();
            }else{
                JOptionPane.showMessageDialog(this, "Data "+nip+" Gagal di hapus !");
            }
            getData();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

